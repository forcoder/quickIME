// 将内置码表数据加载到运行时的辅助函数
inline void LoadBuiltinTable(std::vector<WubiTableEntry>& out) {
    out.clear();
    out.reserve(kLevel1Codes.size() + kLevel2Codes.size());
    for (const auto& e : kLevel1Codes) out.push_back(e);
    for (const auto& e : kLevel2Codes) out.push_back(e);
}

